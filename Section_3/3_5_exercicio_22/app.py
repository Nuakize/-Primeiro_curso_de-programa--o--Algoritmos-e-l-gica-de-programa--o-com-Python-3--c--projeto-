n1 = int(input("Insira um número para comparação "))
n2 = int(input("Insira o segundo número para comparação: "))

if n1 > n2:
    print("O maior número digitado é %.0f" %n1)

if n1 < n2:
    print("O maior número digitado é %.0f" %n2)

if n1 == n2:
    print("Ambos os números apresentados são iguais")